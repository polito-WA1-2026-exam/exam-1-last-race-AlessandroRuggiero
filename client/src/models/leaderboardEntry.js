export function LeaderboardEntry(userId, username, startStation, endStation, answer, startTime, endTime, coins) {
    this.userId = userId;
    this.username = username;
    this.startStation = startStation;
    this.endStation = endStation;
    this.answer = answer;
    this.startTime = startTime;
    this.endTime = endTime;
    this.coins = coins;
}
